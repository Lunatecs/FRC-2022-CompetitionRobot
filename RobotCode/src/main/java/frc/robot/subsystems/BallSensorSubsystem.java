package frc.robot.subsystems;

import edu.wpi.first.wpilibj2.command.SubsystemBase;
import frc.robot.Constants.BallSensorConstants;
import edu.wpi.first.wpilibj.DigitalInput;
import edu.wpi.first.wpilibj.smartdashboard.SmartDashboard;


public class BallSensorSubsystem extends SubsystemBase {
  private final DigitalInput top = new DigitalInput(BallSensorConstants.TOP_SENSOR);
  private final DigitalInput bottom = new DigitalInput(BallSensorConstants.BOTTOM_SENSOR);
  
  /** Creates a new BallSensorSubsystem. */
  public BallSensorSubsystem() {}

  @Override
  public void periodic() {
    LEDSubsystem led = LEDSubsystem.instanceOf();
    if(led!=null) {
      if(!top.get() && !bottom.get()) {
        led.addColor(led.BOTH_BLUE);
        led.removeColor(led.ONE_BLUE);
      } else if (!top.get() || !bottom.get()) {
        led.addColor(led.ONE_BLUE);
        led.removeColor(led.BOTH_BLUE);
      } else {
        led.removeColor(led.BOTH_BLUE);
        led.removeColor(led.ONE_BLUE);
      }
    }
    //SmartDashboard.putBoolean("Top Sensor Activated", top.get());
    //SmartDashboard.putBoolean("Bottom Sensor Activated", bottom.get());
  }
}
